    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String hoten = this.txtHoten.getText();
        String cccd = this.txtCccd.getText();
        String sdt = this.txtSdt.getText();
        String nhamang = this.txtNhamang.getText();
        String goi = this.txtGoi.getText();
        String time = this.txtTime.getText();
        int giacuoc = Integer.parseInt(this.txtGiacuoc.getText());
        if (check == 1){
            list.add(new Quanly(hoten, cccd, sdt, nhamang, goi, time, giacuoc));
            //khi ấn vào add thì = 1
        }
        if(check == -1){
            list.set(pos, new Quanly(hoten, cccd, sdt, nhamang, goi, time, giacuoc));
            // khi ấn vào sửa thì = -1
        }
        View();
        ViewTable();
        
    }//GEN-LAST:event_btnSaveActionPerformed
