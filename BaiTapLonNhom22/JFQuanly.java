    public void View()
    {
        x = list.get(pos);
        this.txtHoten.setText(x.getHoten());
        this.txtCccd.setText(x.getCccd());
        this.txtSdt.setText(x.getSdt());
        this.txtNhamang.setText(x.getNhamang());
        this.txtGoi.setText(x.getGoi());
        this.txtTime.setText(x.getTime());
        this.txtGiacuoc.setText("" + x.getGiacuoc()); // "" + bởi vì là int
        OnOff(true,false);
    }
    // hàm để xét các giá trị được nhập vào bảng table
    public void ViewTable()
    {
        DefaultTableModel model = (DefaultTableModel) this.tblQuanly.getModel();
        model.setNumRows(0);
        int n = 1;
        for(Quanly x:list)
        {
            model.addRow(new Object[]{n++,x.getHoten(),x.getCccd(),x.getSdt(),x.getNhamang(),x.getGoi(),x.getTime(),x.getGiacuoc()});
            // n là stt tăng dần 
        }
        this.tblQuanly.setRowHeight(25);
    }
    
    
